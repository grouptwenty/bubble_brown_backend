import GOBALS from '../GOBALS';
export default class StockOutModel {

    constructor() {
    }

    async insertStockOutByOrder(data) {
        return fetch(GOBALS.URL + '/stockout/insertStockOutByOrder', {
            method: 'POST',
            headers: {
                'Accept': 'application/json',
                'Content-Type': 'application/json'
            },
            body: JSON.stringify(data)
        }).then((response) => response.json())
            .then((responseJson) => {

                return responseJson;
            }).catch((error) => {
                return {
                    data: [],
                    error: error,
                    query_result: false,
                    server_result: false
                };
            });
    }
    async deleteStockOutByOrderCode(data) {
        return fetch(GOBALS.URL + '/stockout/deleteStockOutByOrderCode', {
            method: 'POST',
            headers: {
                'Accept': 'application/json',
                'Content-Type': 'application/json'
            },
            body: JSON.stringify(data)
        }).then((response) => response.json())
            .then((responseJson) => {

                return responseJson;
            }).catch((error) => {
                return {
                    data: [],
                    error: error,
                    query_result: false,
                    server_result: false
                };
            });
    }

  

}